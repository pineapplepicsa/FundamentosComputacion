/* Comparacion de cadenas: Funcion strcmp

Los operadores de comparacion(==, !=, <=, >, >=) NO funcionan con cadenas.

La funcion strcmp recibe dos cadenas, a y b, y devuelve un entero. El entero que resulta de efectuar la
llamada strcmp(a, b) codifica el resultado de la comparacion de la siguiente manera:

- Es menor que cero si la cadena "a" es menor que "b".
- Es 0 si la cadena "a" es igual que "b".
- Es mayor que cero si la cadena "a" es mayor que "b".

Menor significa que va delante en orden alfabetico, y mayor que va detras. */
